const String kDefaultCountry = '中国';

/// 所有国家名列表
final List<String> kCountries = kWorldCities.keys.toList();

/// 获取某国家的省/州列表
List<String> provincesOf(String country) {
  return kWorldCities[country]?.keys.toList() ?? [];
}

/// 获取某国家某省的城市列表
List<String> citiesOf(String country, String province) {
  return kWorldCities[country]?[province] ?? [];
}

/// 城市 → (国家, 省) 反向映射（懒加载）
Map<String, (String, String)>? _cityIndex;
Map<String, (String, String)> get cityIndex {
  _cityIndex ??= {
    for (final countryEntry in kWorldCities.entries)
      for (final provEntry in countryEntry.value.entries)
        for (final city in provEntry.value)
          city: (countryEntry.key, provEntry.key),
  };
  return _cityIndex!;
}

/// 查询城市所属省份（兼容旧数据）
String provinceOf(String city) {
  return cityIndex[city]?.$2 ?? '';
}

/// 模糊搜索城市（跨所有国家）
List<CityMatch> searchCities(String query) {
  if (query.trim().isEmpty) return [];
  final q = query.trim().toLowerCase();
  final results = <CityMatch>[];
  for (final countryEntry in kWorldCities.entries) {
    for (final provEntry in countryEntry.value.entries) {
      for (final city in provEntry.value) {
        if (city.toLowerCase().contains(q)) {
          results.add(CityMatch(
            city: city,
            province: provEntry.key,
            country: countryEntry.key,
          ));
        }
      }
    }
  }
  return results;
}

/// 搜索结果
class CityMatch {
  const CityMatch({
    required this.city,
    required this.province,
    required this.country,
  });
  final String city;
  final String province;
  final String country;
}
